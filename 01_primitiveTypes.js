
//Primitive Data Types
//01. Number
var age = 30;
var weight = 62;
var height = 5.5;
var speckNumber = 0.50;

console.log(age,weight,height,speckNumber);

console.log(typeof age);

//02. Boolean --> true or false

var isMarried = true;
var isSoftEngg = true;
var batchelor = false;
console.log(isMarried,isSoftEngg,batchelor);
console.log(typeof isMarried);

//03. String --> Sequence of character
var firstName = "Rohit";
var lastName = "Shrama";
console.log(firstName, lastName);
console.log(typeof firstName);

//Special Data Types
//01. Undefined
var city = " ";
console.log(typeof city);

var address;
console.log(typeof address);

//02. Null
var pin = null;
console.log("Valaue: ", pin);
console.log("Type: ", typeof pin);