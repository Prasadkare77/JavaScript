console.log(`Compound Addition`);
var num1 = 20;
var num2 = 10;
num1 += num2;
console.log(`Compound Addition is: ${num1}`);

console.log(`Compound Substraction`);
var num1 = 20;
var num2 = 10;
num1 -= num2;
console.log(`Compound Substraction is: ${num1}`);

console.log(`Compound Multiplication`);
var num1 = 20;
var num2 = 10;
num1 *= num2;
console.log(`Compound Multiplication is: ${num1}`);

console.log(`Compound Division`);
var num1 = 20;
var num2 = 10;
num1 /= num2;
console.log(`Compound Division is: ${num1}`);
